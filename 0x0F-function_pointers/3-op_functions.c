/**
 * op_add - sums a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds the difference of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - finds the product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides one integer by another integer
 * @a: dividend integer
 * @b: divisor integer
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds the remainder of dividing one integer by
 * another integer
 * @a: dividend integer
 * @b: divisor integer
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
