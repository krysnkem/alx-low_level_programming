/**
 * add - adds two integers togeter
 * @a: first number
 * @b: second number
 * Return: the sum of two integers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - finds the difference between two integers
 * @a: first number
 * @b: second number
 * Return: the difference between two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - finds the product of two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the product of two integers
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divides two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the whole numbe in the division
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - finds the remainder of division of two integers
 * @a: the first number
 * @b: the second number
 * Return: the remainder of division of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
